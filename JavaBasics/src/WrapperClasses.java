import java.util.ArrayList;

public class WrapperClasses { /*contrast wrapper classes to Python. In Python, "primitive" data types are
								automatically objects. This is not so in Java. 
	*/
	public static void main(String[] args) {
		
		float a = 0.5994f; //f suffix for some reason. 
		float b = 0.6748f; 
		
		Float number_1 = a;
		Float number_2 = b;
		
		System.out.print("Float a: " + number_1 + "\nFloat b: " + number_2); //a little anticlimactic but I'll learn more about this later. 
		
	}

}
